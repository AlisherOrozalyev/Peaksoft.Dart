void main(){
  // Модификатор доступа => key words (var, dynamic, final, const)
  // const бул компиляция учурунда инициализация болот
  const a = 10;
  // final бул программанын аткарылуучунда инициализация болот
  final b = 20; 
  // var автоматтык турдо маанинин тибин алат
  var c = "Жалан стринг алат";
  // dynamic Бул баардык типти ала берет
  dynamic s = "Динамикалуу иштеп баардык типти ала берет";

  print(a);
}